-- Neu database dang trang hoan toan, chi can chay file nay truoc.

CREATE TABLE IF NOT EXISTS VaiTro (
    MaVaiTro INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Code VARCHAR(50) NOT NULL UNIQUE,
    TenHienThi VARCHAR(100) NOT NULL,
    MoTa VARCHAR(255) NOT NULL,
    Quyen VARCHAR(2000),
    ChoPhepTruyCapAdmin BOOLEAN NOT NULL DEFAULT FALSE,
    LaVaiTroHeThong BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE IF NOT EXISTS TaiKhoan (
    MaTaiKhoan INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TenDangNhap VARCHAR(50) NOT NULL UNIQUE,
    MatKhau VARCHAR(255) NOT NULL,
    HoTen VARCHAR(255) NOT NULL,
    Email VARCHAR(100) NOT NULL UNIQUE,
    SoDienThoai VARCHAR(15),
    DiaChi TEXT,
    Avatar VARCHAR(255) NOT NULL DEFAULT 'default.png',
    TrangThai BOOLEAN NOT NULL DEFAULT TRUE,
    NgayTao TIMESTAMP NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS TaiKhoanRole (
    MaTaiKhoan INTEGER NOT NULL,
    MaVaiTro INTEGER NOT NULL,
    PRIMARY KEY (MaTaiKhoan, MaVaiTro),
    CONSTRAINT FK_TaiKhoanRole_TaiKhoan
        FOREIGN KEY (MaTaiKhoan) REFERENCES TaiKhoan (MaTaiKhoan) ON DELETE CASCADE,
    CONSTRAINT FK_TaiKhoanRole_VaiTro
        FOREIGN KEY (MaVaiTro) REFERENCES VaiTro (MaVaiTro) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS DanhMuc (
    MaDanhMuc INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TenDanhMuc VARCHAR(100) NOT NULL UNIQUE,
    MoTa VARCHAR(500)
);

CREATE TABLE IF NOT EXISTS HangSanXuat (
    MaHang INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TenHang VARCHAR(150) NOT NULL UNIQUE,
    Website TEXT,
    MoTa VARCHAR(500)
);

CREATE TABLE IF NOT EXISTS LoaiHang (
    MaLoaiHang INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TenLoaiHang VARCHAR(150) NOT NULL UNIQUE,
    MoTa VARCHAR(500),
    TrangThai BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE IF NOT EXISTS DoChoi (
    MaDoChoi VARCHAR(50) PRIMARY KEY,
    TenDoChoi VARCHAR(300) NOT NULL,
    MaDanhMuc INTEGER NOT NULL,
    MaHang INTEGER,
    HinhAnh TEXT,
    TyLe VARCHAR(100),
    ChatLieu VARCHAR(255),
    NhomHang VARCHAR(150),
    GiaBan BIGINT,
    GiaVon BIGINT,
    TonKho INTEGER,
    TonKhoToiThieu INTEGER,
    TonKhoToiDa INTEGER,
    DonViTinh VARCHAR(50),
    QuyCach VARCHAR(150),
    DangKy BOOLEAN,
    DuocBan BOOLEAN DEFAULT TRUE,
    MoTa TEXT,
    MauGhiChu VARCHAR(255),
    TrangThai BOOLEAN DEFAULT TRUE,
    CreatedAt TIMESTAMP,
    UpdatedAt TIMESTAMP,
    CONSTRAINT FK_DoChoi_DanhMuc
        FOREIGN KEY (MaDanhMuc) REFERENCES DanhMuc (MaDanhMuc),
    CONSTRAINT FK_DoChoi_HangSanXuat
        FOREIGN KEY (MaHang) REFERENCES HangSanXuat (MaHang)
);

CREATE TABLE IF NOT EXISTS BienTheDoChoi (
    MaBienTheDoChoi INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaDoChoi VARCHAR(50) NOT NULL,
    KichThuoc VARCHAR(100) NOT NULL,
    GiaBan BIGINT NOT NULL,
    SoLuongTon INTEGER NOT NULL DEFAULT 0,
    SKU VARCHAR(120),
    TinhTrang VARCHAR(50),
    CONSTRAINT FK_BienThe_DoChoi
        FOREIGN KEY (MaDoChoi) REFERENCES DoChoi (MaDoChoi) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS KhuyenMai (
    MaKhuyenMai VARCHAR(20) PRIMARY KEY,
    TenKhuyenMai VARCHAR(255) NOT NULL,
    SoLuong INTEGER NOT NULL DEFAULT 0,
    PhanTramGiam INTEGER,
    GiamToiDa BIGINT,
    SoTienGiam BIGINT,
    DonToiThieu BIGINT,
    NgayBatDau TIMESTAMP NOT NULL,
    NgayKetThuc TIMESTAMP NOT NULL,
    TrangThai BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE IF NOT EXISTS DonHang (
    MaDonHang INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaTaiKhoan INTEGER,
    MaKhuyenMai VARCHAR(20),
    NgayDat TIMESTAMP NOT NULL DEFAULT NOW(),
    TongTienHang BIGINT,
    PhiVanChuyen BIGINT,
    TienGiamGia BIGINT,
    TongTien BIGINT NOT NULL DEFAULT 0,
    DiaChiGiaoHang VARCHAR(255) NOT NULL,
    TrangThai VARCHAR(50),
    TrangThaiThanhToan VARCHAR(50),
    CONSTRAINT FK_DonHang_TaiKhoan
        FOREIGN KEY (MaTaiKhoan) REFERENCES TaiKhoan (MaTaiKhoan),
    CONSTRAINT FK_DonHang_KhuyenMai
        FOREIGN KEY (MaKhuyenMai) REFERENCES KhuyenMai (MaKhuyenMai)
);

CREATE TABLE IF NOT EXISTS ChiTietDonHang (
    MaChiTiet INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaDonHang INTEGER NOT NULL,
    MaBienTheDoChoi INTEGER NOT NULL,
    SoLuong INTEGER NOT NULL,
    DonGia BIGINT NOT NULL,
    CONSTRAINT FK_ChiTietDonHang_DonHang
        FOREIGN KEY (MaDonHang) REFERENCES DonHang (MaDonHang) ON DELETE CASCADE,
    CONSTRAINT FK_ChiTietDonHang_BienTheDoChoi
        FOREIGN KEY (MaBienTheDoChoi) REFERENCES BienTheDoChoi (MaBienTheDoChoi)
);

CREATE TABLE IF NOT EXISTS DonNhap (
    MaDonNhap INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaTaiKhoan INTEGER NOT NULL,
    NgayNhap TIMESTAMP NOT NULL DEFAULT NOW(),
    TongTienNhap BIGINT NOT NULL DEFAULT 0,
    GhiChu VARCHAR(500),
    CONSTRAINT FK_DonNhap_TK
        FOREIGN KEY (MaTaiKhoan) REFERENCES TaiKhoan (MaTaiKhoan)
);

CREATE TABLE IF NOT EXISTS ChiTietNhap (
    MaChiTietNhap INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaDonNhap INTEGER NOT NULL,
    MaBienTheDoChoi INTEGER NOT NULL,
    SoLuongNhap INTEGER NOT NULL,
    GiaNhap BIGINT NOT NULL,
    CONSTRAINT FK_CTN_DonNhap
        FOREIGN KEY (MaDonNhap) REFERENCES DonNhap (MaDonNhap) ON DELETE CASCADE,
    CONSTRAINT FK_CTN_BienTheDoChoi
        FOREIGN KEY (MaBienTheDoChoi) REFERENCES BienTheDoChoi (MaBienTheDoChoi)
);

CREATE TABLE IF NOT EXISTS GioHang (
    MaGioHang INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaTaiKhoan INTEGER NOT NULL,
    MaBienTheDoChoi INTEGER NOT NULL,
    SoLuong INTEGER NOT NULL DEFAULT 1,
    NgayCapNhat TIMESTAMP NOT NULL DEFAULT NOW(),
    CONSTRAINT UQ_GioHang UNIQUE (MaTaiKhoan, MaBienTheDoChoi),
    CONSTRAINT FK_GioHang_TK
        FOREIGN KEY (MaTaiKhoan) REFERENCES TaiKhoan (MaTaiKhoan) ON DELETE CASCADE,
    CONSTRAINT FK_GioHang_BienTheDoChoi
        FOREIGN KEY (MaBienTheDoChoi) REFERENCES BienTheDoChoi (MaBienTheDoChoi) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS YeuThich (
    MaYeuThich INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaTaiKhoan INTEGER NOT NULL,
    MaDoChoi VARCHAR(50) NOT NULL,
    NgayThem TIMESTAMP NOT NULL DEFAULT NOW(),
    CONSTRAINT UQ_YeuThich UNIQUE (MaTaiKhoan, MaDoChoi),
    CONSTRAINT FK_YeuThich_TK
        FOREIGN KEY (MaTaiKhoan) REFERENCES TaiKhoan (MaTaiKhoan) ON DELETE CASCADE,
    CONSTRAINT FK_YeuThich_DoChoi
        FOREIGN KEY (MaDoChoi) REFERENCES DoChoi (MaDoChoi) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS DanhGia (
    MaDanhGia INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaTaiKhoan INTEGER NOT NULL,
    MaDoChoi VARCHAR(50) NOT NULL,
    SoSao SMALLINT NOT NULL,
    NhanXet TEXT,
    NgayDanhGia TIMESTAMP NOT NULL DEFAULT NOW(),
    TrangThai BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT FK_DanhGia_TK
        FOREIGN KEY (MaTaiKhoan) REFERENCES TaiKhoan (MaTaiKhoan) ON DELETE CASCADE,
    CONSTRAINT FK_DanhGia_DoChoi
        FOREIGN KEY (MaDoChoi) REFERENCES DoChoi (MaDoChoi) ON DELETE CASCADE
);

